package JavaOO;

public class PessoaFisica {
    
    private String cpf;
    private String nome;
    private String telefone;
    private String email;
    private String dataNascimeento;
    private String clienteData; 

    public String getClienteData() {
        return clienteData;
    }

    public void setClienteData(String clienteData) {
        this.clienteData = clienteData;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDataNascimeento() {
        return dataNascimeento;
    }

    public void setDataNascimeento(String dataNascimeento) {
        this.dataNascimeento = dataNascimeento;
    }
    
        
}
